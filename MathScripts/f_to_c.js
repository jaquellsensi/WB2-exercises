//temperature in Fahrenheit
var temperatureInFahrenheit = 68;

// Convert Fahrenheit to Celsius using the formula
var temperatureInCelsius = (temperatureInFahrenheit - 32) * (5 / 9);

console.log(temperatureInFahrenheit + " degrees Fahrenheit is equal to " + temperatureInCelsius.toFixed(2) + " degrees Celsius.");

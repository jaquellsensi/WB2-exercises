var temperatureInCelsius = 20;

// Convert Celsius to Fahrenheit using the formula
var temperatureInFahrenheit = (temperatureInCelsius * (9 / 5)) + 32;

// Print the result
console.log(temperatureInCelsius + " degrees Celsius is equal to " + temperatureInFahrenheit.toFixed(2) + " degrees Fahrenheit.");
